#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second ineteger
 *
 * Return: result of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: evaluation of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: evaluation of operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: evaluation of operation
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulus two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: evaluation of operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
